void GeneratedScene::buildScene(Scene& scene, const SceneDescriptor& desc) {
    scene.buildFromDescriptor(desc);
    generateCodeString(desc);
}

void GeneratedScene::generateCodeString(const SceneDescriptor& desc) {
    std::ostringstream out;
    std::time_t now = std::time(nullptr);
    char timeBuf[64];
    std::strftime(timeBuf,sizeof(timeBuf),"%Y-%m-%d %H:%M:%S",std::localtime(&now));

    out << "// AUTO-GENERATED by Text2Cinema Engine\n"
        << "// Generated: " << timeBuf << "\n"
        << "// Input: \"" << desc.rawText << "\"\n\n"
        << "#include \"scene/Scene.h\"\n"
        << "#include \"simulation/ParticleSystem.h\"\n"
        << "#include \"rendering/Camera.h\"\n\n"
        << "namespace generated {\n\n"
        << "void buildScene(t2c::Scene& scene, t2c::Camera& camera) {\n\n";

    out << "    // Environment: sky=" << desc.environment.sky
        << " ground=" << desc.environment.ground << "\n\n";

    for (const auto& obj : desc.objects) {
        out << "    {\n"
            << "        auto obj = std::make_unique<t2c::SceneObject>(\n"
            << "            t2c::ObjectType::" << capitalise(obj.type)
            << ", \"" << obj.type << "\");\n"
            << "        obj->setScale(" << obj.scale << "f);\n";
        if (desc.motion.verb == "fall") {
            out << "        obj->setPosition({0.0f, 18.0f, 0.0f});\n"
                << "        obj->setVelocity({0.0f, -4.0f, 0.0f});\n";
        }
        out << "        scene.addObject(std::move(obj));\n    }\n\n";
    }

    out << "    camera.setMode(t2c::CameraMode::Tracking);\n"
        << "    camera.setPosition({0.0f, 5.0f, 20.0f});\n\n";

    for (const auto& ev : desc.events) {
        out << "    // Effect: " << ev.effect
            << " trigger=" << ev.trigger << "\n";
    }

    if (desc.hasCameraShake) {
        out << "\n    scene.registerEventCallback("
            << "[&camera](const std::string& type, t2c::SceneObject*) {\n"
            << "        if (type == \"impact\") camera.triggerShake(0.4f, 1.2f);\n"
            << "    });\n";
    }

    out << "\n} // buildScene\n\n} // namespace generated\n";
    m_code = out.str();
}

bool GeneratedScene::saveCode(const std::string& outputPath,
                               const SceneDescriptor&) const
{
    utils::ensureDirectory("generated");
    std::ofstream f(outputPath);
    if (!f.is_open()) {
        std::cerr << "[GeneratedScene] Cannot write: " << outputPath << "\n";
        return false;
    }
    f << m_code;
    std::cout << "[GeneratedScene] Code saved -> " << outputPath << "\n";
    return true;
}

std::string GeneratedScene::capitalise(const std::string& s) {
    if (s.empty()) return s;
    std::string r = s;
    r[0] = static_cast<char>(std::toupper(static_cast<unsigned char>(r[0])));
    return r;
}

